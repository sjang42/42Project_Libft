#include "libft.h"

long long	ft_atoll(const char *str)
{
	long long	nbr;
	int			i;
	char		neg;

	nbr = 0;
	i = 0;
	neg = 1;
	while ((str[i] >= 9 && str[i] <= 13) || (str[i] == 32))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			neg *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nbr *= 10;
		nbr += ((str[i] - '0') * neg);
		i++;
	}
	return (nbr);
}
